    public static int birthday(List<Integer> s, int d, int m) {
    // Write your code here
    	int result = 0;
        int sum = 0;
        
        if(m <= s.size()){
            for(int i = 0; i < m; i++){
                sum += s.get(i);
            }
        }
        
        if(sum == d)
            result++;
        
        
        
        for(int j = 0; j <s.size() - m; j++){
            sum = sum - s.get(j) + s.get(j+m);
            if(sum == d) result++;
        }
    return result;
    }

